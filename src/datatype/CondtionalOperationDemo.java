package datatype;

public class CondtionalOperationDemo {
public static void main(String[] args) {
	// > >= < <= == !=   // always return boolean , true of false
	
	int i = 10;
	int j = 11;
	

//	bitwise operator , And or OR (&   and  |)
	
	System.out.println(i > j);  // false
	System.out.println(i >= j); // false
	System.out.println(i < j); // true
	System.out.println(i <= j); // true
	System.out.println(i == j); // false
	System.out.println(i != j); // true
	
}
}
